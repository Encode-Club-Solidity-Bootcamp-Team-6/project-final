import { useEffect, useState } from "react";
import { parseEther } from "viem";
import { useAccount, useContractReads, useWriteContract } from "wagmi";

const numberOfAssets = 5; // TODO fetch from contract, if possible

export interface Asset {
  priceFeed: string;
  tokenNum: string;
  tokenDen: string;
  tokenAmount: bigint;
  initPrice: bigint;
}

type ContractReadsOutput = {
  data?: any[];
  isError: boolean;
  isLoading: boolean;
};

const useUmbrella = (umbrellaFundAddress: string) => {
  const account = useAccount();
  const [latestTxMessage, setLatestTxMessage] = useState<string>("");
  const { data: hash, isPending, error, writeContract } = useWriteContract();

  useEffect(() => {
    if (isPending) setLatestTxMessage("Transaction pending...");
    else if (error) setLatestTxMessage(`Transaction failed: ${error.message}`);
    else if (hash) setLatestTxMessage(`Transaction included. ${hash}`);
    else setLatestTxMessage("");
  }, [isPending]);

  const umbrellaContract = {
    address: umbrellaFundAddress,
    abi: abi as any,
  } as const;

  const { data, isError, isLoading }: ContractReadsOutput = useContractReads({
    contracts: [
      {
        ...umbrellaContract,
        functionName: "purchaseRatio",
      },
      {
        ...umbrellaContract,
        functionName: "paymentToken",
      },
      {
        ...umbrellaContract,
        functionName: "getTokenShare",
        args: [account.address || ""],
      },
      {
        ...umbrellaContract,
        functionName: "getTotalSupply",
      },
      {
        ...umbrellaContract,
        functionName: "calculateNAVCurrent",
      },
      {
        ...umbrellaContract,
        functionName: "calculateNAVInitial",
      },
      {
        ...umbrellaContract,
        functionName: "calculateReturn",
      },
      {
        ...umbrellaContract,
        functionName: "getTokenNames", // comma separated list of token names
      },
      {
        ...umbrellaContract,
        functionName: "getTokenAmounts", // comma separated list of token amounts
      },
      {
        ...umbrellaContract,
        functionName: "getTokenValues", // comma separated list of token values (amount * price)
      },
    ],
  });

  const tokenContract = {
    address: data?.[1].result as string,
    abi: tokenAbi as any,
  };

  const { data: dataToken } = useContractReads({
    contracts: [
      {
        ...tokenContract,
        functionName: "symbol",
      },
      {
        ...tokenContract,
        functionName: "balanceOf",
        args: [account.address || ""],
      },
    ],
  });

  const getAssets = () => {
    let assets: Asset[] = [];
    for (let i = 0; i < numberOfAssets; i++) {
      const { data: dataAssets } = useContractReads({
        contracts: [
          {
            ...umbrellaContract,
            functionName: "getAsset",
          },
        ],
      });
      if (dataAssets && dataAssets[0].status === "success") assets.push(dataAssets[0].result as Asset);
    }
    return assets;
  };

  /**
   * @param amount - The amount in ether to deposit
   */
  const deposit = (amount: string) => {
    writeContract({
      address: umbrellaFundAddress,
      abi,
      functionName: "deposit",
      value: parseEther(amount),
    });
  };

  /**
   * @param amount - The amount in ether to withdraw
   */
  const withdraw = (amount: string) => {
    writeContract({
      address: umbrellaFundAddress,
      abi,
      functionName: "withdraw",
      args: [parseEther(amount)],
    });
  };

  const buyAsset = (amount: string, index: number) => {
    writeContract({
      address: umbrellaFundAddress,
      abi,
      functionName: "buyAsset",
      args: [parseEther(amount), index],
    });
  };

  const sellAsset = (index: number) => {
    writeContract({
      address: umbrellaFundAddress,
      abi,
      functionName: "sellAsset",
      args: [index],
    });
  };

  // Umbrella
  const purchaseRatio = data && data[0].status === "success" ? data[0].result.toString() : 0;
  const paymentToken = data && data[1].status === "success" ? data[1].result : null;
  const tokenShare = data && data[2].status === "success" ? data[2].result : 0;
  const totalSupply = data && data[3].status === "success" ? data[3].result : 0;
  const currentNAV = data && data[4].status === "success" ? data[4].result : 0;
  const initialNAV = data && data[5].status === "success" ? data[5].result : 0;
  const returnNAV = data && data[6].status === "success" ? data[6].result : 0;
  const tokenNames = data && data[7].status === "success" ? (data[7].result as string).split(",") : [];
  const tokenAmounts = data && data[8].status === "success" ? (data[8].result as string).split(",") : [];
  const tokenValues = data && data[9].status === "success" ? (data[9].result as string).split(",") : [];

  // Token
  const ticker = dataToken && dataToken[0].status === "success" ? (dataToken[0].result as any).toString() : "Unknown";
  const balance = dataToken && dataToken[1].status === "success" ? dataToken[1].result : 0;

  return {
    purchaseRatio,
    umbrellaTokenAddress: paymentToken,
    tokenShare,
    totalSupply,
    currentNAV,
    initialNAV,
    returnNAV,
    getAssets,
    deposit,
    withdraw,
    buyAsset,
    sellAsset,
    tokenNames,
    tokenAmounts,
    tokenValues,
    ticker,
    latestTxMessage,
    balance,
    isError,
    isLoading,
  };
};

export default useUmbrella;

